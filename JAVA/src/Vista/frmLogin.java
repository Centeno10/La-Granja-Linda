    public class BarraProgreso implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent ae) {
            contador++;
            Cargando.setValue(contador);
            if (contador == 100) {
                tiempo.stop();
                if (Cargando.getValue() == 100) {
                    Sistema sis = new Sistema(lg);
                    sis.setVisible(true);
                    dispose();
                }
            }
        }
    }

    public void validar() {
        String nombre = txtUsuario.getText(); // Cambiar de correo a nombre
        String pass = String.valueOf(txtContraseña.getPassword());
        if (!"".equals(nombre) || !"".equals(pass)) {

            lg = login.log(nombre, pass); // Cambiar para pasar nombre en lugar de correo
            if (lg.getNombre() != null && lg.getPass() != null) { // Cambiar de getCorreo() a getNombre()
                Cargando.setVisible(true);
                contador = -1;
                Cargando.setValue(0);
                Cargando.setStringPainted(true);
                tiempo = new Timer(segundos, new BarraProgreso());
                tiempo.start();
            } else {
                JOptionPane.showMessageDialog(null, "Nombre o contraseña incorrectos");
            }
        }
    }

